export async function POST(request: Request) {
  let jobId = randomUUID().slice(0, 8);

  try {
    const form = await request.formData();
    jobId = parseJobId(form.get("clientJobId")) ?? jobId;
    const runMode = String(form.get("runMode") || "video");
    const referenceVideo = form.get("referenceVideo");
    const input = parseInput(form);
    const productImage = productImageFromForm(form.get("productImage"), form, input.productName);
    const usableReferenceVideo = referenceVideo instanceof File && referenceVideo.size > 0
      ? referenceVideo
      : undefined;

    if (input.generationMode !== "fast_ugc" && !usableReferenceVideo) {
      return NextResponse.json({ error: "referenceVideo is required." }, { status: 400 });
    }

    if (!productImage) {
      return NextResponse.json({ error: "productImage is required." }, { status: 400 });
    }

    if (runMode !== "starter" && !hasFalApiKey()) {
      return NextResponse.json(
        { error: "FAL_KEY or FAL_API_KEY is required for fal Kling v3 Pro Image-to-Video generation." },
        { status: 400 }
      );
    }

    input.productImageProvided = true;

    const creatorReferences = await saveCreatorReferences(form, jobId, input);
    input.creatorReferenceProvided = creatorReferences.some((reference) => Boolean(reference.imagePath));

    if (input.modelMode !== "auto_generated" && !input.creatorReferenceProvided) {
      return NextResponse.json(
        { error: "Model reference mode requires a creator image upload." },
        { status: 400 }
      );
    }

    if (
      input.modelMode === "per_account_models"
      && creatorReferences.some((reference) => !reference.imagePath)
    ) {
      return NextResponse.json(
        { error: "Per-account model mode requires one model image for each account." },
        { status: 400 }
      );
    }

    if (runMode === "starter") {
      return createStarterPreviewResponse({
        jobId,
        input,
        referenceVideo: usableReferenceVideo,
        productImage,
        creatorReferences
      });
    }

    const warnings: string[] = [
      "Video engine: fal Kling v3 Pro Image-to-Video.",
      generationModeWarning(input),
      "Native audio is generated by fal Kling with generate_audio=true.",
      input.referenceProductMode === "same_product"
        ? "Product source mode: same product. Reference product handling is preserved when it matches the uploaded product image."
        : "Product source mode: replace product. Reference video structure is reused, but uploaded product image/use plan defines the target product.",
      modelModeWarning(input)
    ];

    const productImagePath = await saveProductImage(productImage, jobId);
    const referenceVideoPath = usableReferenceVideo
      ? await saveUpload(usableReferenceVideo, jobId)
      : undefined;
    const approvedStarterJobId = parseJobId(form.get("approvedStarterJobId"));
    const starterImagePath = input.creatorReferenceProvided
      ? await getApprovedQualityStarterPath(approvedStarterJobId)
      : referenceVideoPath
        ? await createReferenceStarterFrame(referenceVideoPath, jobId, warnings)
        : undefined;

    if (input.creatorReferenceProvided && !starterImagePath) {
      return NextResponse.json(
        { error: "Model reference generation requires an approved starter image first." },
        { status: 400 }
      );
    }

    if (input.creatorReferenceProvided && starterImagePath) {
      warnings.push(
        "Scene starter: using approved quality starter image as start_image_url for model/product/reference composition."
      );
    }

    const referenceVideoAssetProfile = referenceVideoPath && usableReferenceVideo
      ? await buildReferenceVideoAssetProfile({
          filePath: referenceVideoPath,
          mimeType: usableReferenceVideo.type || "video/mp4",
          label: usableReferenceVideo.name || "Reference video"
        })
      : undefined;
    const productAssetProfile = await buildImageAssetProfile({
      kind: "product",
      filePath: productImagePath,
      mimeType: productImage.type || "image/png",
      label: input.productName,
      userGuidance: input.productFeatureNotes,
      analysisMode: input.productAnalysisMode
    });
    const profiledCreatorReferences = await buildCreatorAssetProfiles(creatorReferences);

    input.referenceVideoAssetProfile = referenceVideoAssetProfile;
    input.productAssetProfile = productAssetProfile;
    input.creatorAssetProfile = profiledCreatorReferences.find((reference) => reference.assetProfile)?.assetProfile;

    await writeAssetProfilesDebug(jobId, [
      referenceVideoAssetProfile,
      productAssetProfile,
      ...profiledCreatorReferences
        .map((reference) => reference.assetProfile)
        .filter((profile): profile is AssetProfile => Boolean(profile))
    ]);

    if (referenceVideoAssetProfile) {
      warnings.push(...assetProfileWarnings("Reference video", referenceVideoAssetProfile));
    }
    warnings.push(...assetProfileWarnings("Product", productAssetProfile));
    const tryOnCompatibility = validateTryOnCompatibility(input, productAssetProfile);
    warnings.push(...tryOnCompatibility.warnings);
    if (!tryOnCompatibility.ok) {
      return NextResponse.json(
        { error: tryOnCompatibility.message, warnings: dedupe(warnings) },
        { status: 400 }
      );
    }
    for (const reference of profiledCreatorReferences) {
      if (reference.assetProfile) {
        warnings.push(...assetProfileWarnings(`Model ${reference.accountName}`, reference.assetProfile));
      }
    }

    const analysis = referenceVideoPath && usableReferenceVideo
      ? await analyzeReferenceVideo(referenceVideoPath, usableReferenceVideo.type || "video/mp4", input)
      : buildFastUgcReferenceAnalysis(input);
    const intent = resolveGenerationIntent(input, analysis);
    const assetBinding = buildAssetBindingDebug(input);
    await writeIntentDebug(jobId, intent);
    warnings.push(`Intent resolver: ${intent.productMode.label} / ${intent.videoFormat.name}.`);
    warnings.push("Prompt composer: GPT creates Product Selling Plan and fal Kling generationPrompt from Gemini/Product/Avatar analysis.");

    const plans = await createProductFocusPlans(analysis, input, intent);
    const selectedPlans = plans.slice(0, input.count);
    const videos: GeneratedVideo[] = [];

    for (const plan of selectedPlans) {
      const creatorReference = pickCreatorReference(profiledCreatorReferences, plan.index);

      try {
        const sourceUrl = await generateFalKlingVideo(
          plan.generationPrompt,
          input.duration,
          {
            productImagePath,
            productImageMimeType: productImage.type || "image/png",
            creatorImagePath: creatorReference?.imagePath,
            creatorImageMimeType: creatorReference?.imageMimeType,
            starterImagePath,
            starterImageMimeType: starterImagePath ? mimeTypeFromPath(starterImagePath) : undefined
          },
          {
            generateAudio: true,
            warnings,
            debug: { jobId, index: plan.index }
          }
        );
        const playbackUrl = await persistRemoteVideo(jobId, plan.index, sourceUrl, "fal-kling", warnings);

        videos.push({
          index: plan.index,
          accountName: creatorReference?.accountName,
          hook: plan.hook,
          script: plan.voiceoverScript,
          generationPrompt: plan.generationPrompt,
          playbackUrl: playbackUrl ?? sourceUrl,
          sourceUrl,
          audioMode: "native_audio",
          status: "completed"
        });
      } catch (error) {
        videos.push({
          index: plan.index,
          accountName: creatorReference?.accountName,
          hook: plan.hook,
          script: plan.voiceoverScript,
          generationPrompt: plan.generationPrompt,
          status: "failed",
          error: error instanceof Error ? error.message : "Unknown generation error"
        });
      }
    }

    const createdAt = new Date().toISOString();
    const payload: PipelineResult = {
      jobId,
      inputSnapshot: buildInputSnapshot(input, {
        createdAt,
        productImage,
        referenceVideo: usableReferenceVideo,
        creatorReferences: profiledCreatorReferences,
        approvedStarterJobId
      }),
      analysis,
      intent,
      assetBinding,
      assetProfiles: {
        referenceVideo: referenceVideoAssetProfile,
        product: productAssetProfile,
        creator: input.creatorAssetProfile
      },
      videos,
      warnings: dedupe(warnings)
    };

    await writePipelineResult(payload);
    await writeHistoryEntry({
      jobId,
      title: input.productName || "Untitled product",
      createdAt,
      formatName: input.subFormatName ?? getVideoFormat(input.videoFormat).name,
      styleName: input.generationMode === "fast_ugc"
        ? "Fast UGC"
        : input.generationMode === "reference_match"
          ? "Reference Match"
          : "Winning Ad Remix",
      videoCount: videos.length,
      thumbnailUrl: videos.find((video) => video.playbackUrl || video.sourceUrl)?.playbackUrl
        ?? videos.find((video) => video.playbackUrl || video.sourceUrl)?.sourceUrl
    });
    return NextResponse.json(payload);
  } catch (error) {
    await writeApiErrorDebug(jobId, "POST /api/product-focus/generate", error);
    return NextResponse.json(
      {
        error: error instanceof Error ? error.message : "Generation failed."
      },
      { status: 500 }
    );
  }
}

export async function GET(request: Request) {
  const url = new URL(request.url);
  const jobId = parseJobId(url.searchParams.get("jobId"));

  if (!jobId) {
    return NextResponse.json({ error: "jobId is required." }, { status: 400 });
  }

  const payload = await readPipelineResult(jobId);

  if (!payload) {
    return NextResponse.json({ error: "Result is not ready yet." }, { status: 404 });
  }

  return NextResponse.json(payload);
}

async function writeApiErrorDebug(jobId: string, scope: string, error: unknown): Promise<void> {
  try {
    await mkdir(logsDir, { recursive: true });
    await writeFile(
      path.join(logsDir, `api-error-${jobId}.json`),
      JSON.stringify({
        jobId,
        scope,
        at: new Date().toISOString(),
        error: error instanceof Error
          ? {
              name: error.name,
              message: error.message,
              stack: error.stack
            }
          : error
      }, null, 2),
      "utf8"
    );
  } catch {
    // Preserve the original API failure; diagnostics must not mask it.
  }
}

async function createStarterPreviewResponse(context: StarterPreviewContext) {
  const warnings: string[] = [
    "Quality gate: generated starter preview only. No fal Kling video generation was run."
  ];
  const productImagePath = await saveProductImage(context.productImage, context.jobId);
    const productAssetProfile = await buildImageAssetProfile({
      kind: "product",
      filePath: productImagePath,
      mimeType: context.productImage.type || "image/png",
      label: context.input.productName,
      userGuidance: context.input.productFeatureNotes,
      analysisMode: context.input.productAnalysisMode
    });
  context.input.productAssetProfile = productAssetProfile;
  warnings.push(...assetProfileWarnings("Product", productAssetProfile));

  const tryOnCompatibility = validateTryOnCompatibility(context.input, productAssetProfile);
  if (!tryOnCompatibility.ok) {
    return NextResponse.json(
      {
        error: tryOnCompatibility.message,
        warnings: [...warnings, ...tryOnCompatibility.warnings]
      },
      { status: 400 }
    );
  }

  const referenceVideoPath = context.referenceVideo
    ? await saveUpload(context.referenceVideo, context.jobId)
    : undefined;
  const referenceFramePath = referenceVideoPath
    ? await createReferenceStarterFrame(referenceVideoPath, context.jobId, warnings)
    : undefined;

  if (context.input.generationMode !== "fast_ugc" && !referenceFramePath) {
    return NextResponse.json(
      { error: "Could not extract a reference-video starter frame." },
      { status: 500 }
    );
  }

  const creatorReference = pickCreatorReference(context.creatorReferences, 1);

  if (!creatorReference?.imagePath) {
    return NextResponse.json({
      jobId: context.jobId,
      starterImageUrl: referenceFramePath
        ? runtimeOutputUrl(`${context.jobId}-reference-starter.jpg`)
        : "",
      warnings: [
        ...warnings,
        referenceFramePath
          ? "No model image was provided, so the extracted reference frame is the starter preview."
          : "No model image or reference video was provided, so no starter preview is required."
      ]
    });
  }

  if (!process.env.OPENAI_API_KEY) {
    return NextResponse.json(
      { error: "OPENAI_API_KEY is required to create a model/product quality starter image." },
      { status: 400 }
    );
  }

  const starter = await createQualityStarterImage({
    jobId: context.jobId,
    referenceFramePath,
    productImagePath,
    productImageMimeType: context.productImage.type || "image/png",
    creatorImagePath: creatorReference.imagePath,
    creatorImageMimeType: creatorReference.imageMimeType || "image/png",
    productName: context.input.productName,
    productAssetProfile,
    generationMode: context.input.generationMode,
    videoFormatId: context.input.videoFormat,
    videoFormatName: getVideoFormat(context.input.videoFormat).name
  });

  return NextResponse.json({
    jobId: context.jobId,
    starterImageUrl: starter.localUrl,
    referenceFrameUrl: referenceFramePath ? runtimeOutputUrl(`${context.jobId}-reference-starter.jpg`) : undefined,
    warnings: [
      ...warnings,
      "Quality gate: starter image combines uploaded model identity, uploaded product, Product Use Director placement, and reference-video scene composition."
    ]
  });
}

function parseInput(form: FormData): ProductFocusInput {
  const productName = String(form.get("productName") || "").trim();
  const productUrl = String(form.get("productUrl") || "").trim();
  const productFeatureNotes = englishOnlyText(String(form.get("productFeatureNotes") || "")).trim();
  const productAnalysisMode = productFeatureNotes
    ? "guided"
    : String(form.get("productAnalysisMode") || "auto") === "guided"
      ? "guided"
      : "auto";
  const tone = String(form.get("tone") || "TikTok native").trim();
  const generationMode = parseGenerationMode(String(form.get("generationMode") || "fast_ugc"));
  const requestedSubFormat = getVideoSubFormat(String(form.get("subFormatId") || ""));
  const videoFormat = getVideoFormat(String(form.get("videoFormat") || requestedSubFormat?.videoFormat || "ugc_beauty_product")).id;
  const subFormat = requestedSubFormat?.videoFormat === videoFormat ? requestedSubFormat : undefined;
  const ugcTemplateStyle = parseUgcTemplateStyle(String(form.get("ugcTemplateStyle") || "auto"));
  const modelMode = parseModelMode(String(form.get("modelMode") || "auto_generated"));
  const referenceProductMode = parseReferenceProductMode(String(form.get("referenceProductMode") || "replace_product"));
  const explicitCreatorMode = parseCreatorMode(String(form.get("creatorMode") || "auto"));
  const creatorMode = explicitCreatorMode !== "auto"
    ? explicitCreatorMode
    : modelMode === "auto_generated"
      ? "new_creator"
      : "model_reference";
  const duration = Number(form.get("duration") || 10);
  const count = Math.min(10, Math.max(1, Number(form.get("count") || 3)));

  if (!productName) {
    throw new Error("Product name is required.");
  }

  if (!validDurations.has(duration)) {
    throw new Error("Duration must be 5, 10, or 15 seconds.");
  }

  return {
    generationMode,
    videoFormat,
    subFormatId: subFormat?.id,
    subFormatName: subFormat?.name,
    subFormatIntent: subFormat?.explanation,
    ugcTemplateStyle,
    productName,
    productUrl,
    productFeatureNotes,
    productAnalysisMode,
    productImageProvided: false,
    modelMode,
    creatorMode,
    creatorReferenceProvided: false,
    referenceProductMode,
    audioMode: "native_audio",
    tone,
    duration: duration as 5 | 10 | 15,
    count
  };
}

