function firstFile(form: FormData, names: string[]): File | undefined {
  for (const name of names) {
    const value = form.get(name);
    if (value instanceof File && value.size > 0) {
      return value;
    }
  }

  return undefined;
}

async function persistRemoteVideo(
  jobId: string,
  index: number,
  videoUrl: string,
  label: string,
  warnings: string[]
): Promise<string | undefined> {
  if (!videoUrl.startsWith("http")) {
    return videoUrl;
  }

  try {
    const response = await fetch(videoUrl);

    if (!response.ok) {
      throw new Error(`HTTP ${response.status}`);
    }

    const contentType = response.headers.get("content-type") || "video/mp4";
    const bytes = Buffer.from(await response.arrayBuffer());
    const saved = await saveOutputBuffer(bytes, jobId, `${label}-${index}.mp4`);

    warnings.push(`Video cached locally for browser playback: ${saved.localUrl}`);

    if (!contentType.includes("video") && !contentType.includes("octet-stream")) {
      warnings.push(`Video cache warning: remote content-type was ${contentType}.`);
    }

    return saved.localUrl;
  } catch (error) {
    warnings.push(
      `Video cache warning: could not cache ${label} video locally (${error instanceof Error ? error.message : "unknown error"}). Using remote URL fallback.`
    );
    return undefined;
  }
}

function dedupe(values: string[]): string[] {
  return Array.from(new Set(values));
}

function mimeTypeFromPath(filePath: string): string {
  const extension = path.extname(filePath).toLowerCase();
  if (extension === ".png") return "image/png";
  if (extension === ".webp") return "image/webp";
  return "image/jpeg";
}

async function writePipelineResult(payload: PipelineResult): Promise<void> {
  await mkdir(logsDir, { recursive: true });
  const bytes = Buffer.from(JSON.stringify(payload, null, 2));
  await writeFile(
    path.join(logsDir, `pipeline-result-${payload.jobId}.json`),
    bytes,
    "utf8"
  );

  if (hasR2Config()) {
    await uploadPublicObject(
      publicObjectKey(payload.jobId, `pipeline-result-${payload.jobId}.json`),
      bytes,
      "application/json"
    );
  }
}

async function writeHistoryEntry(entry: ProjectHistoryEntry): Promise<void> {
  await mkdir(logsDir, { recursive: true });

  let previous: ProjectHistoryEntry[] = [];

  try {
    const remoteHistory = await readPublicObject(publicGlobalObjectKey("history-index.json"));
    previous = JSON.parse(remoteHistory?.toString("utf8") ?? await readFile(historyIndexPath, "utf8")) as ProjectHistoryEntry[];
  } catch {
    previous = [];
  }

  const next = [entry, ...previous.filter((item) => item.jobId !== entry.jobId)].slice(0, 100);
  const bytes = Buffer.from(JSON.stringify(next, null, 2));
  await writeFile(historyIndexPath, bytes, "utf8");

  if (hasR2Config()) {
    await uploadPublicObject(
      publicGlobalObjectKey("history-index.json"),
      bytes,
      "application/json"
    );
  }
}

async function readPipelineResult(jobId: string): Promise<PipelineResult | undefined> {
  try {
    const remote = await readPublicObject(publicObjectKey(jobId, `pipeline-result-${jobId}.json`));
    if (remote) return JSON.parse(remote.toString("utf8")) as PipelineResult;

    const raw = await readFile(path.join(logsDir, `pipeline-result-${jobId}.json`), "utf8");
    return JSON.parse(raw) as PipelineResult;
  } catch {
    return reconstructCompletedResult(jobId);
  }
}

async function reconstructCompletedResult(jobId: string): Promise<PipelineResult | undefined> {
  let outputNames: string[];

  try {
    outputNames = await readdir(outputDir);
  } catch {
    return undefined;
  }

  const videos = outputNames
    .map((name) => {
      const match = name.match(new RegExp(`^${jobId}-fal-kling-(\\d+)\\.mp4$`));
      return match ? { name, index: Number(match[1]) } : undefined;
    })
    .filter((item): item is { name: string; index: number } => Boolean(item))
    .sort((a, b) => a.index - b.index)
    .map<GeneratedVideo>((item) => ({
      index: item.index,
      hook: "Recovered completed generation",
      script: "",
      generationPrompt: "",
      playbackUrl: `/outputs/${item.name}`,
      sourceUrl: `/outputs/${item.name}`,
      audioMode: "native_audio",
      status: "completed"
    }));

  if (!videos.length) {
    return undefined;
  }

  return {
    jobId,
    analysis: {
      format: "ugc_beauty_product"
    },
    videos,
    warnings: [
      "Recovered from local output cache after the browser request disconnected.",
      "Detailed analysis/prompt data was not available for this older job because result persistence was added after it ran."
    ]
  };
}
